def get_system_prompt(profile: dict) -> str:
    if not profile.get("setup_done"):
        return _BASE

    level = profile.get("level", "beginner")
    os_name = profile.get("os", "Mac")
    goal = profile.get("goal", "изучить Claude Code")

    if level == "beginner":
        level_desc = "полный новичок — объясняй с нуля, не пропускай шаги, не используй жаргон без объяснения"
    else:
        level_desc = "есть базовый опыт — можно идти быстрее, пропускать очевидные вещи"

    return _BASE + f"""
---

## ПРОФИЛЬ ПОЛЬЗОВАТЕЛЯ
- *Уровень:* {level_desc}
- *ОС:* {os_name} — давай команды только для этой системы. Не показывай варианты для других ОС.
- *Цель:* {goal} — держи в уме, связывай обучение с этой целью где возможно.

Все объяснения, команды и примеры — под этот профиль. На {os_name} используй соответствующий синтаксис.
"""
